export function getPhrasesByCategory(category: PhraseCategory): Phrase[] {
  return PHRASES.filter((p) => p.category === category);
}

/** Deterministic filename for a phrase's cached audio on the server.
 *  Stored at: data/audio/{phraseId}-{language}-{speaker}.txt  */
export function phraseAudioFilename(phraseId: string, language: string, speaker: string): string {
  return `${phraseId}-${language}-${speaker}.txt`;
}
